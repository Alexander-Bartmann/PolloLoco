/**
 * Class representing the keyboard input state
 * @class
 */
class Keyboard {
    /** @type {boolean} - State of left movement key */
    left = false;
    /** @type {boolean} - State of right movement key */
    right = false;
    /** @type {boolean} - State of up movement key */
    up = false;
    /** @type {boolean} - State of down movement key */
    down = false;
    /** @type {boolean} - State of space/jump key */
    space = false;
    /** @type {boolean} - State of throw action key */
    r = false;
}